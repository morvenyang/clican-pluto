package com.clican.appletv.core.service.taobao.model;

public class TaobaoFareRequestOrderItem {

	private String item;
	private String quantity;
	private boolean itemPostFree;
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public boolean isItemPostFree() {
		return itemPostFree;
	}
	public void setItemPostFree(boolean itemPostFree) {
		this.itemPostFree = itemPostFree;
	}
	
	
}
